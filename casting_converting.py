num = 13

# Convert an integer to binary string
# Prefix is 0b
# Calls __index__()
print(bin(num))

# Formatting to include '0b' prefix
print(f'{num:#b}')

# Formatting to exclude '0b' prefix
print(f'{num:b}')

#------------------------------------------------------------------------------
#------------------------------------------------------------------------------

# Returns Boolean, True or False
# Value is converted using the standard truth testing procedure
print(bool(3))
print(bool(0))
print(bool(True))
print(bool('a'))
print(bool([]))

#------------------------------------------------------------------------------
#------------------------------------------------------------------------------

# Converts Unicode index to character
# [32 - 47] ==> symbols
# [48 - 57] ==> 0-9 digits
# [58 - 64] ==> symbols
# [65 - 90] ==> A-Z letters
# [91 - 96] ==> symbols
# [97 - 122] ==> a-z letters
# [123 - ...] ==> symbols
print(chr(97))

